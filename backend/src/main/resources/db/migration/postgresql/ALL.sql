-- =============================================================
-- RBAC 权限模型 (PostgreSQL 14+)
-- 用户 - 角色（多对多）、角色 - 菜单（多对多）
-- =============================================================

-- 角色表
CREATE TABLE IF NOT EXISTS roles (
                                     id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                                     role_code   VARCHAR(50)  NOT NULL,
    role_name   VARCHAR(50)  NOT NULL,
    description VARCHAR(255),
    built_in    BOOLEAN      NOT NULL DEFAULT FALSE,
    created_at  TIMESTAMPTZ  NOT NULL DEFAULT now(),
    updated_at  TIMESTAMPTZ  NOT NULL DEFAULT now(),
    deleted     BOOLEAN      NOT NULL DEFAULT FALSE
    );
CREATE UNIQUE INDEX IF NOT EXISTS uk_role_code ON roles (role_code) WHERE deleted = FALSE;

-- 菜单表（parent_id=0 表示顶级；menu_type: DIR 目录 / MENU 菜单）
CREATE TABLE IF NOT EXISTS menus (
                                     id         BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                                     parent_id  BIGINT       NOT NULL DEFAULT 0,
                                     menu_key   VARCHAR(100) NOT NULL,
    name       VARCHAR(50)  NOT NULL,
    path       VARCHAR(200),
    sort       INT          NOT NULL DEFAULT 0,
    menu_type  VARCHAR(20)  NOT NULL DEFAULT 'MENU',
    created_at TIMESTAMPTZ  NOT NULL DEFAULT now(),
    updated_at TIMESTAMPTZ  NOT NULL DEFAULT now(),
    deleted    BOOLEAN      NOT NULL DEFAULT FALSE
    );
CREATE UNIQUE INDEX IF NOT EXISTS uk_menu_key ON menus (menu_key) WHERE deleted = FALSE;

-- 用户-角色关联
CREATE TABLE IF NOT EXISTS user_roles (
                                          id         BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                                          user_id    BIGINT      NOT NULL,
                                          role_id    BIGINT      NOT NULL,
                                          created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT now(),
    deleted    BOOLEAN     NOT NULL DEFAULT FALSE
    );
CREATE UNIQUE INDEX IF NOT EXISTS uk_user_role ON user_roles (user_id, role_id);

-- 角色-菜单关联
CREATE TABLE IF NOT EXISTS role_menus (
                                          id         BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                                          role_id    BIGINT      NOT NULL,
                                          menu_id    BIGINT      NOT NULL,
                                          created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT now(),
    deleted    BOOLEAN     NOT NULL DEFAULT FALSE
    );
CREATE UNIQUE INDEX IF NOT EXISTS uk_role_menu ON role_menus (role_id, menu_id);

